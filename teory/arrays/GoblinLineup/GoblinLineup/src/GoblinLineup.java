public class GoblinLineup {
    public static void main(String[] args) {

        int[] globins = new int[5];
        globins[0] = 10;
        globins[1] = 12;
        globins[2] = 34;
        globins[3] = 54;
        globins[4] = 22;

        for (int n : globins){
            System.out.print(n + " ");
        }
    }
}
